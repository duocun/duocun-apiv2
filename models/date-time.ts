  // myLocalTime -- '2020-03-23T23:58:00'
  // ms --- list of moment objects
  // return the latest moment object after my LocalTime
  getLatestMoment(myLocalTime: string, ms: moment.Moment[]) {
    const now = this.getMomentFromLocal(myLocalTime);
    const a = ms.filter(oe => oe.isAfter(now));

    if (a.length === 1) {
      return a[0];
    } else {
      let latest = a[0];
      for (let i = 1; i < a.length; i++) {
        if (a[i].isBefore(latest)) {
          latest = a[i];
        }
      }
      return latest;
    }
  }
